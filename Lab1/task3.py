def weather_forecast(day):
    """Функция возвращает сумму средней температуры, атмосферного давления и влажности для указанного дня."""
    if day < 1 or day > 10:
        raise ValueError("day должен быть числом от 1 до 10 включительно")

    # Данные с первой метеостанции: температура и атмосферное давление
    weather_station1 = {
        1: [20, 735],
        2: [21, 737],
        3: [23, 740],
        4: [20, 739],
        5: [21, 750],
        6: [21, 751],
        7: [22, 749],
        8: [19, 745],
        9: [22, 752],
        10: [23, 753]
    }

    # Данные со второй метеостанции: температура и влажность
    weather_station2 = {
        1: [20, 89],
        2: [20, 90],
        3: [24, 95],
        4: [20, 88],
        5: [21, 87],
        6: [21, 80],
        7: [22, 72],
        8: [18, 80],
        9: [22, 75],
        10: [23, 78]
    }

    # Получаем данные для указанного дня
    temp1, pressure = weather_station1[day]
    temp2, humidity = weather_station2[day]

    # Рассчитываем среднюю температуру
    avg_temp = (temp1 + temp2) / 2

    # Возвращаем сумму средней температуры, давления и влажности
    return avg_temp + pressure + humidity

if __name__ == "__main__":
    # Пример использования
    print(weather_forecast(1))
    print(weather_forecast(4))