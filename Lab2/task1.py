import re

def function(string):
  """
  Подсчитывает слова, начинающиеся с "о" или "э" в строке.

  Args:
    string: Исходная строка.

  Returns:
    Количество найденных слов.
  """

  # Регулярное выражение для поиска слов, начинающихся с "о" или "э"
  pattern = r"\b[оэОЭ]\w+"

  # Поиск всех совпадений с шаблоном в строке
  matches = re.findall(pattern, string)

  # Возврат количества найденных совпадений
  return len(matches)

# Примеры использования функции
print(function('Сказала Ольга: "Это верно ,что 2+2=4"'))  # Вывод: 2
print(function('Это надо знать. Это - классика'))  # Вывод: 2