import re

def function(string):
    """
    Подсчитывает количество последовательностей букв в строке.

    Args:
        string: Исходная строка.

    Returns:
        Количество найденных последовательностей.
    """

    # Регулярное выражение для поиска последовательностей букв с дефисами
    pattern = r"[a-zA-Zа-яА-ЯёЁ]+(?:-[a-zA-Zа-яА-ЯёЁ]+)*"

    # Поиск всех совпадений с шаблоном в строке
    matches = re.findall(pattern, string)

    # Возврат количества найденных совпадений
    return len(matches)

# Примеры использования функции
print(function("""Он --- серо-буро-малиновый слон!!>>>:->А не кот."""))  # Вывод: 6
print(function("""Он - человек!!>>>:->А не кот."""))  # Вывод: 5